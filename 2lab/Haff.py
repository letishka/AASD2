REV_DC = {code: cat for cat, code in DC_HUFFMAN.items()}
REV_AC = {code: (run, cat) for (run, cat), code in AC_HUFFMAN.items()}

def huffman_encode_dc(category):
    # Вход: целое число от 0 до 11
    # Выход: строка битов (или пустая строка, если категория не найдена)

    if category in DC_HUFFMAN:
        return DC_HUFFMAN[category]
    return ""

def huffman_encode_ac(run, category):
    # Вход: run (0..15), category (0..10)
    # Выход: строка битов

    key = (run, category)
    if key in AC_HUFFMAN:
        return AC_HUFFMAN[key]
    return ""

def bits_to_bytes(bit_string):
    pad = (8 - len(bit_string) % 8) % 8
    bit_string += "0" * pad
    out = bytearray()
    for i in range(0, len(bit_string), 8):
        byte_val = 0
        for j in range(8):
            if bit_string[i+j] == '1':
                byte_val |= (1 << (7 - j))
        out.append(byte_val)
    return bytes(out)

def bytes_to_bits(byte_data):
    return ''.join(f'{b:08b}' for b in byte_data)

def huffman_decode_dc(bitstring, pos=0):
    code = ""
    while pos < len(bitstring):
        code += bitstring[pos]
        pos += 1
        if code in REV_DC:
            return REV_DC[code], pos
    return None, pos

def huffman_decode_ac(bitstring, pos=0):
    code = ""
    while pos < len(bitstring):
        code += bitstring[pos]
        pos += 1
        if code in REV_AC:
            return REV_AC[code], pos
    return None, pos

if __name__ == "__main__":
    # Тест DC Хаффмана
    cats = [2, 0, 3, 1]
    bitstream = ""
    for c in cats:
        bitstream += huffman_encode_dc(c)
    pos = 0
    decoded_cats = []
    while pos < len(bitstream):
        cat, pos = huffman_decode_dc(bitstream, pos)
        decoded_cats.append(cat)
    assert decoded_cats == cats, "DC Huffman decode failed"
    print("DC Huffman decode OK")

    # Тест AC Хаффмана
    pairs = [(0,2), (1,1), (0,0)]
    bitstream = ""
    for r, c in pairs:
        bitstream += huffman_encode_ac(r, c)
    pos = 0
    decoded_pairs = []
    while pos < len(bitstream):
        pair, pos = huffman_decode_ac(bitstream, pos)
        decoded_pairs.append(pair)
    assert decoded_pairs == pairs, "AC Huffman decode failed"
    print("AC Huffman decode OK")